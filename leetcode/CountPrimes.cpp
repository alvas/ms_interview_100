#include <iostream>

using namespace std;

class Solution {
public:
    int countPrimes(int n) {
        int count = 0;

        if (n <= 1)
        {
            return count;
        }

        return count;
    }
};

int main()
{
    Solution sln;
    int n = 0;
    cout << sln.countPrimes(n) << endl;
    return 0;
}
